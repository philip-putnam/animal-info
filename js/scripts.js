$(document).ready(function() {

  $("#turtleImg").click(function() {
    $(".col-md-12").hide();
    $("#turtles").show();
  });

  $("#snakeImg").click(function() {
    $(".col-md-12").hide();
    $("#snakes").show();
  });

  $("#insectImg").click(function() {
    $(".col-md-12").hide();
    $("#insects").show();
  });

});
